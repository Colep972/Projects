                <h2 class="section3-title"> QU’EST-CE QUE SONT LES REDUCTIONS ET CREDITS D’IMPÔT ? </h2>
            </div>
            <div>
                <p class="section3-text">
                    Reconnaissant le rôle inestimable que jouent les services à domicile dans l'amélioration du bien-être et de l'autonomie des individus, le gouvernement français a mis en place un système complet d'aides fiscales pour encourager leur utilisation.<br><br>

                    Ces incitations se présentent sous deux formes principales : <b>les réductions d'impôt</b> et les <b>crédits d'impôt</b>, chacune offrant des avantages distincts et répondant à des besoins spécifiques.
                </p>
            </div>
            <br>
            <div>
                <h2 class="section3-title"> LES REDUCTIONS D’IMPOT </h2>
            </div>
            <div class="section3-text">
                <p>
                    <b>Les réductions d'impôt</b> permettent de diminuer directement votre impôt sur le revenu. Elles concernent les dépenses engagées pour des services d'aide à la personne, comme le ménage, la garde d'enfants ou le soutien aux personnes âgées dépendantes.<br><br>
                    <b>La réduction d'impôt</b> concerne un large éventail de services à la personne, tels que :
                </p>
                <br>
                <ul class="section3-list-container">
                    <li>Le ménage</li>

                    <li>La garde d'enfants</li>

                    <li>Le soutien aux personnes âgées dépendantes</li>

                    <li>Les cours à domicile</li>

                    <li> Les petits travaux de jardinage </li>
                </ul>
                <br>
            </div>
            <br>
            <div>
                <h2 class="section3-title"> QUELLES SONT LES CONDITIONS D’ELIGIBILITE DES REDUCTIONS D’IMPOT ? </h2>
            </div>
            <div class="section3-text">
                <p>
                    Pour bénéficier de la <b>réduction d'impôt</b>, vous devez respecter certaines conditions, notamment :
                </p>
                <br>
                <ul class="section3-list-container">
                    <li> Etre le particulier qui paie la prestation ou faire appel à une structure de prestations de services à domicile – comme Couleurs Services,</li>

                    <li> Avoir une résidence principale en France</li>

                    <li> Avoir payé la prestation par chèque, CESU ou carte bancaire</li>

                    <li> Avoir conservé les factures et les justificatifs de paiement</li>

                    <li> Les petits travaux de jardinage </li>
                </ul>
                <br>
                <p class="section3-text">
                    <b>Les réductions d'impôt</b> peuvent être accordées à tous les contribuables, y compris ceux qui ne sont pas imposables.<br><br>
                    <b>Les crédits d'impôt</b> peuvent être accordés aux contribuables domiciliés ou résidents en France qui emploient un salarié à domicile.
                </p>
                <br>
            </div>
            <div>
                <h2 class="section3-title"> QUEL EST LE MONTANT DE LA REDUCTION D’IMPOT ? </h2>
            </div>
            <div>
                <p class="section3-text">
                    Le taux de <b>réduction d'impôt</b> est de 25 % des dépenses admissibles, plafonné à une limite annuelle de 10 000 € par personne recevant le service. Il est aussi essentiel de conserver les factures et les justificatifs de paiement.
                </p>
            </div>
            <br>
            <div>
                <h2 class="section3-title"> LES CREDITS D’IMPOT </h2>
            </div>
            <div>
                <p class="section3-text">
                    Si les <b>réductions d’impôt</b> sont un avantage sur l’impôt sur le revenu, <b>les crédits d'impôt</b> sont quant à eux remboursés directement par l'Etat, même si vous n'êtes pas imposable. Ils concernent les dépenses engagées pour l'emploi d'un salarié à domicile, comme un aide-ménagère, une garde d'enfants ou un auxiliaire de vie. Il est aussi essentiel de conserver les factures et les justificatifs de paiement.
                </p>
            </div>
            <br>
            <div>
                <h2 class="section3-title"> QUELLES SONT LES CONDITIONS D’ELIGIBILITE DES CREDITS D’IMPOT ? </h2>
            </div>
            <div class="section3-text">
                <p>
                    Pour bénéficier du <b>crédit d'impôt</b>, vous devez respecter certaines conditions, notamment :
                </p>
                <br>
                <ul class="section3-list-container">
                    <li>Etre le particulier employeur du salarié à domicile ou faire appel à une structure de prestations de services à domicile – comme Couleurs Services,</li>

                    <li>Avoir une résidence principale en France</li>

                    <li>Avoir déclaré le salarié à domicile auprès des organismes sociaux</li>

                    <li>Avoir payé le salaire du salarié par chèque, CESU ou virement bancaire</li>

                    <li>Avoir conservé les factures et les justificatifs de paiement</li>
                </ul>
            </div>
            <div>
                <br>
                <h2 class="section3-title"> QUEL EST LE MONTANT DU CREDIT D’IMPOT ? </h2>
            </div>
            <div>
                <p class="section3-text">
                    <b>Le crédit d'impôt</b> s'élève à 50% des dépenses engagées, dans la limite d'un plafond annuel qui dépend de votre situation. Le plafond de base est de 12 000 € et peut être majoré en fonction de votre situation familiale et de vos revenus.
                </p>
            </div>
            <br>
